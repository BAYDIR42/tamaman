#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(int ac, char **av)
{
	int i;
	int j;

	if (ac != 3)
	{
		ft_putchar('\n');
		return (0);
	}
	i = 0;
	j = 0;
	while (av[2][i] != '\0' && av[1][j] != '\0')
	{
		if (av[1][j] == av[2][i])
			j++;
		i++;
	}
	ft_putchar(*(av[1][j] == '\0' ? "1" : "0"));
	return (0);
}
